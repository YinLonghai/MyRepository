package com.guotion.common.media;

/**
 * Song 
 * 
 * this is the message set for song
 * 
 * @author wenliang Created :2011.06.22
 */

public class Song {
    /**
     * fileName
     */
    private String mFileName = "";

    /**
     * song name
     */
    private String mFileTitle = "";

    /**
     * play total time
     */
    private int mDuration = 0;

    /**
     * singer
     */
    private String mSinger = "";

    /**
     * album name
     */
    private String mAlbum = "";

    /**
     * mYear
     */
    private String mYear = "";

    /**
     * mFileType
     */
    private String mFileType = "";

    /**
     * mFileSize
     */
    private String mFileSize = "";

    /**
     * mFilePath
     */
    private String mFilePath = "";

    /**
     * getmFileName()
     * 
     * @return
     */
    public String getmFileName() {
        return mFileName;
    }

    /**
     * setmFileName()
     * 
     * @param mFileName
     */
    public void setmFileName(String mFileName) {
        this.mFileName = mFileName;
    }

    /**
     * getmFileTitle()
     * 
     * @return
     */
    public String getmFileTitle() {
        return mFileTitle;
    }

    /**
     * setmFileTitle()
     * 
     * @param mFileTitle
     */
    public void setmFileTitle(String mFileTitle) {
        this.mFileTitle = mFileTitle;
    }

    /**
     * getmDuration()
     * 
     * @return
     */
    public int getmDuration() {
        return mDuration;
    }

    /**
     * setmDuration()
     * 
     * @param mDuration
     */
    public void setmDuration(int mDuration) {
        this.mDuration = mDuration;
    }

    /**
     * getmSinger()
     * 
     * @return
     */
    public String getmSinger() {
        return mSinger;
    }

    /**
     * setmSinger()
     * 
     * @param mSinger
     */
    public void setmSinger(String mSinger) {
        this.mSinger = mSinger;
    }

    /**
     * getmAlbum()
     * 
     * @return
     */
    public String getmAlbum() {
        return mAlbum;
    }

    /**
     * setmAlbum()
     * 
     * @param mAlbum
     */
    public void setmAlbum(String mAlbum) {
        this.mAlbum = mAlbum;
    }

    /**
     * getmYear()
     * 
     * @return
     */
    public String getmYear() {
        return mYear;
    }

    /**
     * setmYear()
     * 
     * @param mYear
     */
    public void setmYear(String mYear) {
        this.mYear = mYear;
    }

    /**
     * getmFileType()
     * 
     * @return
     */
    public String getmFileType() {
        return mFileType;
    }

    /**
     * setmFileType()
     * 
     * @param mFileType
     */
    public void setmFileType(String mFileType) {
        this.mFileType = mFileType;
    }

    /**
     * getmFileSize()
     * 
     * @return
     */
    public String getmFileSize() {
        return mFileSize;
    }

    /**
     * setmFileSize()
     * 
     * @param mFileSize
     */
    public void setmFileSize(String mFileSize) {
        this.mFileSize = mFileSize;
    }

    /**
     * getmFilePath()
     * 
     * @return
     */
    public String getmFilePath() {
        return mFilePath;
    }

    /**
     * setmFilePath()
     * 
     * @param mFilePath
     */
    public void setmFilePath(String mFilePath) {
        this.mFilePath = mFilePath;
    }
}
